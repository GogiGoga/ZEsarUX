	poke_byte(HL,bytehl);

        //FLAG C no alterado
        Z80_FLAGS = Z80_FLAGS & FLAG_C;
        
        Z80_FLAGS |=sz53p_table[reg_a];


	set_memptr(reg_temp_hl+1);
	
