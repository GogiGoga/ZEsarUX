        contend_read_no_mreq( HL, 1 ); contend_read_no_mreq( HL, 1 );


	low_hl=bytehl & 0xF;
	high_hl=(bytehl >> 4) & 0xF;

	low_a_copia=low_a=reg_a & 0xF;

	//este sin rotar
	high_a=reg_a & 0xF0;

	low_a=high_hl;
	high_hl=low_hl;
	low_hl=low_a_copia;
	
	reg_a=high_a | low_a;
